        private void btnEgit_Click(object sender, EventArgs e)
        {
            if (ag == null)
            {
                MessageBox.Show("Önce Tanımla butonuna basın.");
                return;
            }

            double epsilon = (double)nudEpsilon.Value;
            int maxEpoch = 10000;

            int epoch = 0;
            double hata;

            do
            {
                ag.Train(egitimGirdiler, egitimCiktilar, 1);
                epoch++;

                hata = 0;
                for (int k = 0; k < egitimGirdiler.Length; k++)
                {
                    var y = ag.FeedForward(egitimGirdiler[k]);
                    for (int i = 0; i < y.Length; i++)
                        hata += Math.Pow(egitimCiktilar[k][i] - y[i], 2);
                }
                hata /= egitimGirdiler.Length;

                Application.DoEvents();
            }
            while (hata > epsilon && epoch < maxEpoch);


            double[] sonuc = ag.FeedForward(girisDizisi);

            lblCikisA.Text = $"A çıkışı = {sonuc[0]:F6}";
            lblCikisB.Text = $"B çıkışı = {sonuc[1]:F6}";
            lblCikisC.Text = $"C çıkışı = {sonuc[2]:F6}";
            lblCikisD.Text = $"D çıkışı = {sonuc[3]:F6}";
            lblCikisE.Text = $"E çıkışı = {sonuc[4]:F6}";

            int maxIndex = 0;
            for (int i = 1; i < sonuc.Length; i++)
                if (sonuc[i] > sonuc[maxIndex])
                    maxIndex = i;
            lblTahmin.Text = $"Tahmin edilen harf: {"ABCDE"[maxIndex]}";

            MessageBox.Show($"Eğitim tamamlandı.\nEpoch sayısı: {epoch}\nSon hata (MSE): {hata:F6}");
        }




